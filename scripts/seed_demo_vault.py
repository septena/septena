DAY_ROTATION = [UPPER_DAY, CARDIO_DAY, LOWER_DAY, CARDIO_DAY]  # 4-day cycle


def seed_exercise(cfg: SeedConfig) -> None:
    rnd = random.Random(cfg.seed + 1)
    log_dir = cfg.vault / "Exercise/Log"
    _ensure_clean(log_dir)

    # One session every other day (so ~15 over 30 days)
    for offset in range(0, cfg.days, 2):
        day = cfg.today - timedelta(days=offset)
        template = DAY_ROTATION[offset // 2 % len(DAY_ROTATION)]
        concluded = datetime.combine(day, datetime.min.time()).replace(hour=8, minute=rnd.randint(0, 30))

        for slug, fields in template:
            fm: dict[str, Any] = {
                "date": day.isoformat(),
                "exercise": slug,
                "concluded_at": concluded.isoformat(timespec="seconds"),
            }
            # Gentle progression: +0.5kg per week on strength
            for k, v in fields.items():
                if k == "weight" and isinstance(v, float):
                    weeks_back = offset // 7
                    v = round(v - weeks_back * 0.5, 1)
                fm[k] = v
            fm["difficulty"] = rnd.choice(["easy", "medium", "medium", "hard"])

            fname = f"{day.isoformat()}--{slug.replace(' ', '-')}--01.md"
            (log_dir / fname).write_text(_dump_frontmatter(fm))


