def seed_nutrition(cfg: SeedConfig) -> None:
    rnd = random.Random(cfg.seed)
    log_dir = cfg.vault / "Nutrition/Log"
    _ensure_clean(log_dir)

    # Pick 3 meals/day + ~40% of days get a snack, spread across 30 days.
    # Use disjoint pools so the same day doesn't repeat a meal.
    breakfasts = [m for m in MEALS if m[0].startswith("07") or m[0].startswith("08")]
    lunches    = [m for m in MEALS if m[0].startswith("12") or m[0].startswith("13")]
    snacks     = [m for m in MEALS if m[0].startswith("16")]
    dinners    = [m for m in MEALS if m[0].startswith("19") or m[0].startswith("20")]

    for offset in range(cfg.days):
        day = cfg.today - timedelta(days=offset)
        todays = [rnd.choice(breakfasts), rnd.choice(lunches), rnd.choice(dinners)]
        if rnd.random() < 0.4:
            todays.append(rnd.choice(snacks))
        todays.sort(key=lambda m: m[0])

        for i, (time_str, emoji, foods, (p, f, c, k)) in enumerate(todays, start=1):
            # Small per-entry jitter so charts aren't flat.
            jitter = lambda v: max(0, int(v * (0.85 + rnd.random() * 0.3)))
            fm = {
                "date": day.isoformat(),
                "time": time_str,
                "emoji": emoji,
                "protein_g": jitter(p),
                "fat_g": jitter(f),
                "carbs_g": jitter(c),
                "kcal": jitter(k),
                "foods": foods,
                "section": "nutrition",
            }
            fname = f"{day.isoformat()}--{time_str.replace(':', '')}--{i:02d}.md"
            (log_dir / fname).write_text(_dump_frontmatter(fm))


