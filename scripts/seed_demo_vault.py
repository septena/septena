#!/usr/bin/env python3
"""Seed a Setlist vault with deterministic demo data.

Generates ~30 days of realistic-looking but fake logs across Nutrition,
Exercise, Habits, and Supplements. Used by the screenshot script and by
anyone running `npm run demo` to see the app with data before logging
their own.

Usage:
    python3 scripts/seed_demo_vault.py --vault examples/demo-vault/Bases
    python3 scripts/seed_demo_vault.py --vault /tmp/demo --today 2026-04-15 --days 45

The script is idempotent: running it again overwrites the Log/ folders
cleanly. It never touches files outside the target vault.
"""

from __future__ import annotations

import argparse
import random
import shutil
import sys
from dataclasses import dataclass
from datetime import date, datetime, timedelta
from pathlib import Path
from typing import Any

import yaml


@dataclass
class SeedConfig:
    vault: Path
    today: date
    days: int
    seed: int = 42


# ── Section writers ─────────────────────────────────────────────────────────

def _dump_frontmatter(fm: dict[str, Any]) -> str:
    body = yaml.safe_dump(fm, sort_keys=False, allow_unicode=True).strip()
    return f"---\n{body}\n---\n"


def _ensure_clean(dir_: Path) -> None:
    """Remove Log/ if it exists, then recreate empty. Preserves any
    sibling config files or .base files in the section folder."""
    if dir_.exists():
        shutil.rmtree(dir_)
    dir_.mkdir(parents=True, exist_ok=True)


def _write_config(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        "# Generated by scripts/seed_demo_vault.py — edit to taste.\n"
        + yaml.safe_dump(payload, sort_keys=False, allow_unicode=True)
    )


